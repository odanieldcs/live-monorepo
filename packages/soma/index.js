// função de soma de dois números
const sum = (a, b) => a + b;

// func'ão de soma de 3 números
const sumWithThreeNumbers = (a, b, c) => a + b + c;

// função de soma de 4 números
const sumWithFourNumbers = (a, b, c, d) => a + b + c + d;

module.exports = {
	sum,
	sumWithThreeNumbers,
	sumWithFourNumbers,
};
